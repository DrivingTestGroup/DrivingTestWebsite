          <p class="que">Who can bring me for my written (G1) test in Ontario?</p>

          <p class='ans'>If you are under the age of 18, a parent or legal guardian must bring you.</p>

          <p class="que">Will a vision test be necessary when I go in to apply for my Ontario driver’s licence?</p>

          <p class='ans'>Absolutely. The vision test is required when you go in to take your written test. It is all a part of acquiring your G1 licence.</p>

          <p class="que">Where do I go to get my Ontario driver’s licence picture taken?</p>

          <p class='ans'>Your driver’s licence photo will be taken at your local MTO office.</p>

          <p class="que">How do I get a licence in Ontario?</p>

          <p class='ans'>Applying for a licence in Ontario is about a 24-month process. There are steps put into place that allow you to easily transition into becoming a responsible licenced driver. You will…

First: Take and pass a written exam that will give you a G1 licence.

Second: Hold your G1 licence for at least 12 months. (Certain restrictions apply with this licence such as no driving without a G licenced driver with at least 4 years of experience in the front seat with you at all times), OR take a driver’s education course at a driving school and wait only 8 months

Third: Take and pass your G1 licence road test (including parallel parking and a few other things). Obtain your G2 licence.

Fourth: Hold your G2 licence at least 12 months. Then apply for your regular G licence.</p>


